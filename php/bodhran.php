			<p>Hääldus: baʊrɑːn (BOW-ran). Bodhran on iiri keelne sõna ja see oma otseselt tähenduselt märgibki trummi või tamburiini. Kui inglese keelest lähtuda, märgib sõna esimene pool “bow” nagu vibu või laeva vööri. Selle sõna etümoloogia muud variatsioonid hõlmavad seoseid sõnadega "nahakandik", "tuim" ja isegi "kurt", kuna arvatakse, et see on seotud sõnaga bodhar, mis tähendab "tuim" või "kurt". <br>
			Bodhran ehk iiri trumm, see raamtrummi põhitüüp on laialt levinud nii Lääne-Aasias kui Lõuna-Indias, osaliselt ka Ida-Euroopas, Põhja-Aafrikas ja Brasiilias, kus seda kasutakse peamiselt pärimusmuusikas. Tema päritolu kohta on erinevaid seisukohti. Arvatakse, et trumm on pärit Aafrikast ja jõudnud Iirimaale Hispaania kaudu. Teisalt, et trumm pärineb Kesk-Aasiast ja toodi Iirimaale kelti sisserändajate poolt või siis hoopis pärineb Iirimaalt ning on aegade jooksul saanud tänase välimuse ja kuju. Käib tuline arutelu selle üle, millal täpselt iiri trumm leiutati, ja paljud inimesed peavad trummi muusikalist esiletõusmist suhteliselt uuemaks nähtuseks. Trumm tõusis esile 1960. aastate keldi muusika taaselustamise ajal (peamiselt tänu populaarse iiri muusiku Seán Ó Riada pilli kasutamisele), varem mängisid seda tänavaparaadidel enamasti noored poisid. Ajakirja Irish Music toimetaja Ronan Nolani välja pakutud teooria on aga see, et iiri trumm arenes välja leiutisest, mida tuntakse "vaese mehe tamburiinina", mis valmistati põllumasinate osadest ja millel puudusid tamburiinidel regulaarselt nähtud taldrikud.  Samas on aga just ka öeldud, et varasemal ajal oli trummile lisatud ka metallist kettakesed, mis tegi ta tamburiiniga sarnaseks.</p>	
