				<p>Igaljuhul tänasel bodhran`il kõlisevad lisandid puuduvad. Trummil on ümmargune puidust raam, mis on seestpoolt tugevdatud. Raamil on tavaliselt üks või kaks risttuge, mis hoiavad ära kõverdumise ja pakuvad ka käele tuge. Trummi läbimõõt varieerub 25-65 cm ning sügavus on keskmiselt 9-20 cm. Trumm kaetakse ühelt poolt tavaliselt kitsenahaga, mis kinnitatakse kas jäigalt või võruga. Viimasel juhul on võimalik ka membraani pingutada ja tuua paremini esile madalamaid ja kõrgemaid helisid. Bodhrani raam on tavaliselt ringikujuline, kuid on olemas ka teisi kujusid (need ei ole nii levinud).
