			Mandoliin pärineb 17.sajandist. Eestisse jõudis see arvatavasti 19.sajandil, kui pill levis moepilline laialt üle Euroopa ja Ameerika. Tõenäoliselt algselt hakkasid seda mängima linnakodanikud alles 19.-20.sajandi vahetusel.Kuju on laenatud lautolt, häälestus viiulilt. Väga levinud rahvapill. On leidnud tee nii ooperi-, kammer- ja sümfooniaorkestri muusikasse. 1930.ndate lõpus propageeriti seda koolipillina. Moodustati kooliorkestreid, kus kasutati ka eri suuruses, just orkestrimänguks kujundatud mandoliine, sealhulgas suuri mandoolasid. Sama kordus 1950.–1960. aastatel. Nii levis mandoliin tasapisi rahvapärasesse musitseerimisse. Külaansamblites ja mujalgi mängiti mandoliini meelsasti koos kandle, viiuli ja kitarriga. 
