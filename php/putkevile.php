		<h2 class="h2_pages">Putkevile meisterdamine</h2>
		<div class="grid_pillid">
		<div class="pilli_info_2">
			<p>Putkevile meisterdati heinateo ajal, mil vastav taime varss oli piisavalt tugev ning seest tühjema poolsem. (Teatri ja muusikamuuseum). Eelpool mainitud vilepilli on üsna lihtne teha ja meisterdata. (Tõnurist 1996, 10-34). Pilli materjali korjates kevadel või suvel on vajalik meeles pidada, et värske putkematerjali valikul tuleb vältida mürgiseid putkesid(karuputk jne). Peale suve on võimalik putki korjata ka sügisel või talvisel/kevadisel perioodil, kus putked on kuivanud(nn mürgid kinni jäänud) ning nii on võimalik vältida mürgitusjuhtumeid. Eelpool mainitud perioodil on enamjaolt pea kõik putked või putkelaadsed taimed  sobilikuks pillimaterjaliks ning ohutud mängida. (Kippar, 16)<br><br>
			Valmistades putkevile, siis järgnevalt on putkevarre keskelt vaja lõigata varss pooleks. Lõigata tuleks nii, et üks osa putkevile osast jääks kinniseks ja teine pool avatuks. Olenevalt pikkusest ja ka teatud juhul jämedusest, on võimalik saada kätte erinevaid helikõrgusi. Kõige algelisem putkevile ongi valmis, kuid lisaks võib ülemisele lahtisele otsale teha veel 2 lisa lõiget(nn kaks teraviku). Eelpool mainitud lõigetega tuleks lahtise osa üks pool jätta kõrgemaks ja teine madalamaks. Putkevile mängitakse nõnda, et pilli avatud üks ots asetatakse vastu alumist huult ning puhutakse eelpool mainitud vilepilli, millele järgnev heli. (Tõnurist 1993)</p>
