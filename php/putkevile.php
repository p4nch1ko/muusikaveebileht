			Putkevile kuulub oma ehituselt paaniflöödi tüüpi vilede alla.(Tõnurist 1996, 34). Esimesed pilli kasutajad võisid olla põlluharijad kui ka karjakasvatajad, kelle vahetus ümbruses leidus putkevile tegemiseks sobilikke taimi. Eelpool mainitud vilepilli valmistatakse peamiselt putkedest, siis Eesti aladel tehti neid ka pilliroost ja heinaputkedest. (Tõnurist 1993; Tõnurist 1996).  Lisaks on võimalik teha ka näiteks bambusest ja konnatatrast. <br><br>
