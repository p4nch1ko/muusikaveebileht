     <section>
       <h3>Viiuli variatsioonid</h3>
       <p>Kõige sarnasem pill folk stiilis viiulile on klassikaline viiul. Ehituslikult neil tegelikult vahet
		polegi, sest erinevus seisneb põhiliselt just hoopis mängutehnikas ning kasutusfunktsioonis. <br>
		Väike erinevus klassikalise viiulimängu ning folk stiili vahel seisneb näiteks võimalikus
		pillihoiakus – folk stiilis viiulit (fiddle) võib mängida ka näiteks kaenlas või toetades seda
		rinnale (nii mängivad sageli just pillimehed vanadel fotodel). Sellisel puhul pole oluline ka
		õlatoe olemasolu. Mõnikord puudub sellistel puhkudel ka viiuli lõuatugi, kuna see ei leia
		praktilist väärtust. Pilli rinnal või kaenlas hoidmine annab pillimehele võimaluse ka end
		paremini liigutada – tantsumuusika saatel saab viiulimängija ise kaasa tantsida või hoopis
		laulda.<br>
		Pillimängu õpiti reeglina kas omal käel või mõnelt küla osavamalt pillimeistrilt,
		viiulimängu õpiti enamasti kuulmise järgi, mitte noodist (nagu see on enamasti klassikalise
		viiuliõppe puhul).<br>
		Sarnaseid pille leidub nii Eestis kui ka lähiümbruses – näiteks varem
		nimetatud viiuli Araabiamaadest pärit esivanem rebab (vähem keeli kui viiulil, pilli toetatakse
		rinnale ning õlale), kontrabass (viiulist suurem, mängitakse toetades pilli alaosa maha), viola
		da gamba (sarnane kontrabassile, kuid rohkem keeli), hiiu kannel (viiulist erinev häälestus,
		pilli hoitakse põlvedel, ka pilli kuju on kandilisem ja lihtsam) ning nuppviiul (neli keelt ja iga
		keele juures veel mõned resonantskeeled, pilli hoitakse lapiti üle rinna ja põlvede, mängitakse
		poognaga).<br>
		Üsna sarnase ehitustehnika ning algmaterjaliga pillid teenisid sarnaseid eesmärke
		– tantsumuusika mängimine pidudel ja pulmades. Pillide kvaliteetsemaks muutumisel hakati
		tegelema ka ühismängimisega ning loodi erinevaid pilliansambleid ja -rühmi. Nii leiab
		tänapäeval keelpille rohkelt nii tantsulistes rahvamuusikaansamblites kui ka klassikalistes
		sümfooniaorkestrites. Kuid muidugi leidub tänapäeval ka neid osavaid pillimehi, kes ka
		täiesti üksi folkmuusikat mängides tantsusaalid lõbusalt tantsima löövad.
